<script setup lang="ts">
import type {
  HitokotoItem,
  HitokotoForm,
  HitokotoTypeSelectOption,
} from "@/types/hitokoto";
import type { FormSubmitEvent } from "@nuxt/ui";
import { AxiosError } from "axios";
import { reactive } from "vue";
import { z } from "zod";
import {
  getHitokotoTypeOptions,
  createHitokoto,
  updateHitokoto,
} from "@/apis/hitokoto";

const toast = useToast();

const emits = defineEmits(["cancel", "submit"]);

const schema = z.object({
  id: z.number().optional(),
  type: z.number({ coerce: true }).optional(),
  source: z.string().optional(),
  author: z.string().optional(),
  content: z.string({ message: "请输入内容" }).min(1, "请输入内容"),
});

const formData = reactive<HitokotoForm>({
  content: undefined,
});

const resetForm = () => {
  Object.keys(formData).forEach((key) => {
    formData[key as keyof HitokotoForm] = undefined;
  });
};

const visible = ref(false);

const handleOpen = (data?: HitokotoItem) => {
  resetForm();

  if (data) {
    formData.id = data.id;
    formData.type = data.type;
    formData.source = data.source;
    formData.author = data.author;
    formData.content = data.content;
  }

  visible.value = true;
};

defineExpose({
  open: handleOpen,
});

const submitting = ref(false);
const formRef = useTemplateRef("formRef");
const handleSubmit = async (
  event: FormSubmitEvent<z.output<typeof schema>>
) => {
  try {
    submitting.value = true;

    if (event.data.id) {
      await updateHitokoto({ id: event.data.id, ...event.data });

      toast.add({
        title: "修改成功",
        color: "success",
      });
    } else {
      await createHitokoto({ ...event.data });

      toast.add({
        title: "添加成功",
        color: "success",
      });
    }

    visible.value = false;

    emits("submit");
  } catch (error) {
    let title = "操作失败";
    if (error instanceof AxiosError) {
      title = `操作失败：${error.response?.data?.message ?? error.message}`;
    }

    toast.add({
      title,
      color: "error",
    });
  } finally {
    submitting.value = false;
  }
};

const handleConfirm = async () => {
  formRef.value?.submit();
};

const handleCancel = () => {
  visible.value = false;
  emits("cancel");
};

const hitokotoTypeOptions = ref<HitokotoTypeSelectOption[]>([]);

onMounted(async () => {
  hitokotoTypeOptions.value = await getHitokotoTypeOptions();
});
</script>
<template>
  <UModal
    v-model:open="visible"
    :title="formData.id ? '修改语句' : '添加语句'"
    :ui="{ footer: 'justify-end', overlay: 'bg-elevated/40' }"
  >
    <template #body>
      <UForm
        class="space-y-2"
        ref="formRef"
        :schema="schema"
        :state="formData"
        :validateOnInputDelay="100"
        @submit="handleSubmit"
      >
        <UFormField name="type" label="语句分类">
          <USelect
            class="w-full"
            valueKey="id"
            labelKey="name"
            placeholder="请选择语句分类"
            v-model="formData.type"
            :items="hitokotoTypeOptions"
          />
        </UFormField>
        <UFormField name="source" label="语句来源">
          <UInput
            class="w-full"
            v-model="formData.source"
            placeholder="请输入语句来源"
          />
        </UFormField>
        <UFormField name="author" label="语句作者">
          <UInput
            class="w-full"
            v-model="formData.author"
            placeholder="请输入语句作者"
          />
        </UFormField>
        <UFormField name="content" label="语句内容" required>
          <UTextarea
            class="w-full"
            v-model="formData.content"
            placeholder="请输入语句内容"
          />
        </UFormField>
      </UForm>
    </template>

    <template #footer>
      <UButton
        label="取消"
        color="neutral"
        variant="outline"
        @click="handleCancel"
        :disabled="submitting"
      />
      <UButton
        label="确认"
        color="primary"
        @click="handleConfirm"
        :loading="submitting"
      />
    </template>
  </UModal>
</template>
